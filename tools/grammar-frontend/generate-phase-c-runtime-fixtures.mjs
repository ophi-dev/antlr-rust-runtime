function extractLeftRecursiveAltAssertions(method, language) {
  const values =
    language === "java"
      ? [...method.matchAll(/assertEquals\(\s*"(\[[^"]+\])"/gu)].map(
          (match) => match[1],
        )
      : [
          ...method.matchAll(
            /expect\([^)]*(?:PrimaryAlts|RecursiveOpAlts)[^)]*\)[\s\S]*?toEqual\(\s*(\[[^\]]+\])/gu,
          ),
        ].map((match) => match[1]);
  if (values.length < 2) {
    const arrays = [...method.matchAll(/"\[(?<values>[0-9, ]+)\]"/gu)].map(
      (match) => `[${match.groups.values}]`,
    );
    values.push(...arrays);
  }
  if (values.length < 2) {
    return null;
  }
  return {
    primary: parseNumberArray(values[0]),
    recursive: parseNumberArray(values[1]),
  };
}

function extractIntervalAssertions(method, language) {
  const assertions = [];
  const pattern =
    language === "java"
      ? /assertEquals\(\s*"(?<expected>-?\d+\.\.-?\d+)"\s*,\s*t(?<path>(?:\.getChild\(\d+\))*)\.getSourceInterval\(\)\.toString\(\)\s*\)/gu
      : /expect\(\s*t(?<path>(?:\.getChild\(\d+\)!?)*)\.getSourceInterval\(\)\.toString\(\)\s*\)\.toBe\(\s*"(?<expected>-?\d+\.\.-?\d+)"\s*\)/gu;
  for (const match of method.matchAll(pattern)) {
    const before = method.slice(0, match.index);
    const checkIndex = calls(before, "testInterp", language).length - 1;
    const path = [...match.groups.path.matchAll(/getChild\((\d+)\)/gu)].map(
      (child) => Number.parseInt(child[1], 10),
    );
    assertions.push({
      checkIndex,
      path,
      expected: match.groups.expected,
    });
  }
  return assertions;
}

function firstExpectedError(method, language) {
  const assertions =
    language === "java" ? calls(method, "assertEquals", language) : [];
  if (language === "java") {
    for (const assertion of assertions) {
      if (assertion.arguments[1]?.includes(".errors")) {
        return evaluateString(
          assertion.arguments[0],
          method,
          assertion.offset,
          language,
        );
      }
    }
  }
  const match = /\.toBe\(\s*("(?:\\.|[^"\\])*")\s*\)/gu.exec(method);
  return match ? decodeString(match[1], language) : null;
}

function extractExpectedAltErrors(method, language) {
  const errors = new Map();
  if (language !== "java") {
    return errors;
  }

  const masked = maskCode(method);
  for (const match of masked.matchAll(/\btry\s*\{/gu)) {
    const tryOpen = masked.indexOf("{", match.index);
    const tryClose = matchingDelimiter(masked, tryOpen, "{", "}");
    const catchMatch =
      /\s*catch\s*\(\s*NoViableAltException\s+(?<name>[A-Za-z_$][\w$]*)\s*\)\s*\{/uy;
    catchMatch.lastIndex = tryClose + 1;
    const caught = catchMatch.exec(masked);
    if (!caught) {
      continue;
    }
    const catchOpen = masked.indexOf("{", caught.index);
    const catchClose = matchingDelimiter(masked, catchOpen, "{", "}");
    const catchBody = method.slice(catchOpen + 1, catchClose);
    const variable = escapeRegExp(caught.groups.name);
    const tokenIndex = assertionInteger(
      catchBody,
      new RegExp(
        `assertEquals\\(\\s*(-?\\d+)\\s*,\\s*${variable}\\.getOffendingToken\\(\\)\\.getTokenIndex\\(\\)\\s*\\)`,
        "u",
      ),
      "offending token index",
    );
    const tokenType = assertionInteger(
      catchBody,
      new RegExp(
        `assertEquals\\(\\s*(-?\\d+)\\s*,\\s*${variable}\\.getOffendingToken\\(\\)\\.getType\\(\\)\\s*\\)`,
        "u",
      ),
      "offending token type",
    );
    const checked = calls(
      method.slice(tryOpen + 1, tryClose),
      "checkMatchedAlt",
      language,
    );
    if (checked.length !== 1) {
      throw new Error(
        `expected one checkMatchedAlt call in NoViableAltException try block, found ${checked.length}`,
      );
    }
    const absoluteOffset = tryOpen + 1 + checked[0].offset;
    errors.set(absoluteOffset, { tokenIndex, tokenType });
  }
  return errors;
}

function assertionInteger(source, pattern, label) {
  const match = pattern.exec(source);
  if (!match) {
    throw new Error(`cannot extract ${label} assertion`);
  }
  return Number.parseInt(match[1], 10);
}

function lexerRuleSkips(grammar, name) {
  const masked = maskCode(grammar);
  const declaration = new RegExp(
    `(?:^|\\n)\\s*(?:fragment\\s+)?${escapeRegExp(name)}\\b`,
    "u",
  ).exec(masked);
  if (!declaration) {
    return false;
  }
  const bodyStart = topLevelDelimiter(masked, declaration.index, ":");
  const bodyEnd = topLevelDelimiter(masked, bodyStart + 1, ";");
  return /->\s*skip\b/u.test(masked.slice(bodyStart + 1, bodyEnd));
}

function topLevelDelimiter(source, start, delimiter) {
  const stack = [];
  const matching = new Map([
    [")", "("],
    ["]", "["],
    ["}", "{"],
  ]);
  for (let index = start; index < source.length; index += 1) {
    const current = source[index];
    if ("([{".includes(current)) {
      stack.push(current);
    } else if (matching.has(current)) {
      if (stack.pop() !== matching.get(current)) {
        throw new Error("unbalanced grammar rule");
      }
    } else if (current === delimiter && stack.length === 0) {
      return index;
    }
  }
  throw new Error(`cannot find top-level ${delimiter}`);
}

function adaptParserGrammar(grammar, lexerName) {
  if (/\btokenVocab\s*=/u.test(grammar)) {
    return grammar;
  }
  const declaration =
    /^\s*parser\s+grammar\s+[A-Za-z_]\w*\s*;\s*(?:\r?\n)?/u.exec(grammar);
  if (!declaration) {
    throw new Error(`cannot adapt parser grammar: ${grammar.slice(0, 80)}`);
  }
  const rest = grammar.slice(declaration[0].length);
  const options = /^\s*options\s*\{/u.exec(rest);
  if (options) {
    const open = rest.indexOf("{", options.index);
    return (
      grammar.slice(0, declaration[0].length + open + 1) +
      ` tokenVocab=${lexerName};` +
      rest.slice(open + 1)
    );
  }
  return (
    grammar.slice(0, declaration[0].length) +
    `options { tokenVocab=${lexerName}; }\n` +
    rest
  );
}

function constructorArguments(text, className, language) {
  const result = [];
  const masked = maskCode(text);
  const pattern = new RegExp(`\\bnew\\s+${className}\\s*\\(`, "gu");
  for (const match of masked.matchAll(pattern)) {
    const open = masked.indexOf("(", match.index);
    const close = matchingDelimiter(masked, open, "(", ")");
    result.push({
      offset: match.index,
      arguments: splitArguments(
        text.slice(open + 1, close),
        masked.slice(open + 1, close),
      ),
      language,
    });
  }
  return result;
}

function calls(text, name, language) {
  const result = [];
  const masked = maskCode(text);
  const pattern = new RegExp(`\\b${escapeRegExp(name)}\\s*\\(`, "gu");
  for (const match of masked.matchAll(pattern)) {
    const open = masked.indexOf("(", match.index);
    const close = matchingDelimiter(masked, open, "(", ")");
    result.push({
      offset: match.index,
      arguments: splitArguments(
        text.slice(open + 1, close),
        masked.slice(open + 1, close),
      ),
      language,
    });
  }
  return result;
}

function evaluateString(
  expression,
  source,
  before,
  language,
  seen = new Set(),
) {
  const trimmed = expression.trim();
  if (
    trimmed.startsWith('"') ||
    trimmed.startsWith("`") ||
    /^(?:new\s+StringBuilder|String\.fromCodePoint|Utils\.join)/u.test(trimmed)
  ) {
    return stringExpression(trimmed, language);
  }
  if (/^[A-Za-z_$][\w$]*$/u.test(trimmed)) {
    if (seen.has(trimmed)) {
      throw new Error(`cyclic string binding ${trimmed}`);
    }
    seen.add(trimmed);
    return evaluateString(
      lastAssignment(source, trimmed, before),
      source,
      before,
      language,
      seen,
    );
  }
  return stringExpression(trimmed, language);
}

function evaluateAssignedString(name, source, before, language) {
  return evaluateString(name, source, before, language);
}

function evaluateInteger(expression, source, before, language) {
  const trimmed = expression.trim();
  if (/^-?\d+$/u.test(trimmed)) {
    return Number.parseInt(trimmed, 10);
  }
  const assigned = lastAssignment(source, trimmed, before).trim();
  if (!/^-?\d+$/u.test(assigned)) {
    throw new Error(`unsupported integer expression: ${assigned}`);
  }
  return Number.parseInt(assigned, 10);
}

function evaluateStringArray(expression, source, before, language) {
  const trimmed = expression.trim();
  const resolved = /^[A-Za-z_$][\w$]*$/u.test(trimmed)
    ? lastAssignment(source, trimmed, before)
    : trimmed;
  const masked = maskCode(resolved);
  const opening = language === "java" ? "{" : "[";
  const closing = language === "java" ? "}" : "]";
  const open = masked.indexOf(opening);
  if (open < 0) {
    throw new Error(`unsupported string-array expression: ${resolved}`);
  }
  const close = matchingDelimiter(masked, open, opening, closing);
  return splitArguments(
    resolved.slice(open + 1, close),
    masked.slice(open + 1, close),
  )
    .map((element) => element.trim())
    .filter(Boolean)
    .map((element) => stringExpression(element, language));
}

function lastAssignment(source, variable, before) {
  const masked = maskCode(source);
  const pattern = new RegExp(
    `(?:(?:String|const|let)\\s+)?\\b${escapeRegExp(variable)}\\s*=`,
    "gu",
  );
  const assignments = [...masked.matchAll(pattern)].filter(
    (match) => match.index < before,
  );
  const assignment = assignments.at(-1);
  if (!assignment) {
    throw new Error(`cannot find assignment for ${variable}`);
  }
  const start = assignment.index + assignment[0].length;
  const end = statementEnd(masked, start);
  return source.slice(start, end);
}

function statementEnd(masked, start) {
  const stack = [];
  const matching = new Map([
    [")", "("],
    ["]", "["],
    ["}", "{"],
  ]);
  for (let index = start; index < masked.length; index += 1) {
    const current = masked[index];
    if ("([{".includes(current)) {
      stack.push(current);
    } else if (matching.has(current)) {
      if (stack.pop() !== matching.get(current)) {
        throw new Error("unbalanced assignment expression");
      }
    } else if (current === ";" && stack.length === 0) {
      return index;
    }
  }
  throw new Error("unterminated assignment");
}

function stringExpression(expression, language) {
  const values = stringLiterals(expression, language, true);
  let result = values.join("");
  for (const match of expression.matchAll(
    /(?:appendCodePoint|String\.fromCodePoint)\(\s*(0x[0-9a-f]+|\d+)\s*\)/giu,
  )) {
    result += String.fromCodePoint(Number.parseInt(match[1], 0));
  }
  if (/Utils\.join/u.test(expression)) {
    const separator = values.at(-1);
    return values.slice(0, -1).join(separator);
  }
  return result;
}

function stringLiterals(expression, language, allowEmpty = false) {
  const quotes =
    language === "typescript" ? new Set(['"', "'", "`"]) : new Set(['"']);
  const values = sourceStringLiterals(expression, quotes).map((literal) =>
    decodeString(literal, language),
  );
  if (values.length === 0 && !allowEmpty) {
    throw new Error(`expression has no string literals: ${expression}`);
  }
  return values;
}

function sourceStringLiterals(source, quotes) {
  const literals = [];
  for (let index = 0; index < source.length; index += 1) {
    if (source[index] === "/" && source[index + 1] === "/") {
      index = source.indexOf("\n", index + 2);
      if (index < 0) {
        break;
      }
      continue;
    }
    if (source[index] === "/" && source[index + 1] === "*") {
      const close = source.indexOf("*/", index + 2);
      if (close < 0) {
        throw new Error("unterminated block comment in string expression");
      }
      index = close + 1;
      continue;
    }
    const quote = source[index];
    if (!quotes.has(quote)) {
      continue;
    }
    const start = index;
    for (index += 1; index < source.length; index += 1) {
      if (source[index] === "\\") {
        index += 1;
      } else if (source[index] === quote) {
        literals.push(source.slice(start, index + 1));
        break;
      }
    }
    if (index >= source.length) {
      throw new Error(`unterminated ${quote} literal in string expression`);
    }
  }
  return literals;
}

function decodeString(literal, language) {
  const quote = literal[0];
  let result = "";
  for (let index = 1; index < literal.length - 1; index += 1) {
    const current = literal[index];
    if (current !== "\\") {
      result += current;
      continue;
    }
    const escaped = literal[++index];
    const simple = {
      b: "\b",
      f: "\f",
      n: "\n",
      r: "\r",
      t: "\t",
      v: "\v",
      '"': '"',
      "'": "'",
      "`": "`",
      "\\": "\\",
    };
    if (Object.hasOwn(simple, escaped)) {
      result += simple[escaped];
    } else if (escaped === "u") {
      if (literal[index + 1] === "{") {
        const close = literal.indexOf("}", index + 2);
        const digits = literal.slice(index + 2, close);
        result += String.fromCodePoint(Number.parseInt(digits, 16));
        index = close;
      } else {
        while (literal[index + 1] === "u" && language === "java") {
          index += 1;
        }
        const digits = literal.slice(index + 1, index + 5);
        result += String.fromCodePoint(Number.parseInt(digits, 16));
        index += 4;
      }
    } else {
      throw new Error(
        `unsupported ${language} string escape \\${escaped} in ${quote} literal`,
      );
    }
  }
  return result;
}

function extractJavaMethods(text) {
  const methods = new Map();
  const masked = maskCode(text);
  const pattern =
    /@Test\s+(?:public\s+)?void\s+(?<name>[$\w]+)\s*\([^)]*\)[^{]*\{/gu;
  for (const match of masked.matchAll(pattern)) {
    const open = masked.indexOf("{", match.index);
    const close = matchingDelimiter(masked, open, "{", "}");
    methods.set(match.groups.name, text.slice(open + 1, close));
  }
  return methods;
}

function extractTypeScriptMethods(text) {
  const methods = new Map();
  const masked = maskCode(text);
  const pattern = /\bit(?:\.skip)?\(\s*"(?<name>[^"]+)"/gu;
  for (const match of text.matchAll(pattern)) {
    const arrow = masked.indexOf("=>", match.index);
    const open = masked.indexOf("{", arrow);
    const close = matchingDelimiter(masked, open, "{", "}");
    methods.set(match.groups.name, text.slice(open + 1, close));
  }
  return methods;
}

function splitArguments(text, masked) {
  const result = [];
  let start = 0;
  const stack = [];
  const matching = new Map([
    [")", "("],
    ["]", "["],
    ["}", "{"],
  ]);
  for (let index = 0; index < masked.length; index += 1) {
    const current = masked[index];
    if ("([{".includes(current)) {
      stack.push(current);
    } else if (matching.has(current)) {
      if (stack.pop() !== matching.get(current)) {
        throw new Error("unbalanced argument expression");
      }
    } else if (current === "," && stack.length === 0) {
      result.push(text.slice(start, index));
      start = index + 1;
    }
  }
  if (stack.length !== 0) {
    throw new Error("unterminated argument expression");
  }
  result.push(text.slice(start));
  return result;
}

function matchingDelimiter(text, open, opening, closing) {
  if (open < 0 || text[open] !== opening) {
    throw new Error(`cannot find opening ${opening}`);
  }
  let depth = 0;
  for (let index = open; index < text.length; index += 1) {
    if (text[index] === opening) {
      depth += 1;
    } else if (text[index] === closing && --depth === 0) {
      return index;
    }
  }
  throw new Error(`unterminated ${opening}${closing} block`);
}

function maskCode(text) {
  const output = [...text];
  let state = "code";
  for (let index = 0; index < text.length; index += 1) {
    const current = text[index];
    const next = text[index + 1];
    if (state === "code") {
      if (current === "/" && next === "/") {
        output[index] = output[index + 1] = " ";
        state = "line-comment";
        index += 1;
      } else if (current === "/" && next === "*") {
        output[index] = output[index + 1] = " ";
        state = "block-comment";
        index += 1;
      } else if (current === '"' || current === "'" || current === "`") {
        output[index] = " ";
        state =
          current === '"'
            ? "string"
            : current === "'"
              ? "character"
              : "template";
      }
    } else if (state === "line-comment") {
      if (current === "\n") {
        state = "code";
      } else {
        output[index] = " ";
      }
    } else if (state === "block-comment") {
      if (current === "*" && next === "/") {
        output[index] = output[index + 1] = " ";
        state = "code";
        index += 1;
      } else if (current !== "\n") {
        output[index] = " ";
      }
    } else if (current === "\\") {
      output[index] = " ";
      if (text[index + 1] !== "\n") {
        output[index + 1] = " ";
      }
      index += 1;
    } else if (
      (state === "string" && current === '"') ||
      (state === "character" && current === "'") ||
      (state === "template" && current === "`")
    ) {
      output[index] = " ";
      state = "code";
    } else if (current !== "\n") {
      output[index] = " ";
    }
  }
  return output.join("");
}

function parseArguments(args) {
  const result = {
    update: null,
    javaRoot:
      process.env.ANTLR4_TOOL_ROOT ?? "/tmp/antlr-cleanroom/antlr4-4.13.2-tool",
    antlrNgRoot:
      process.env.ANTLR_NG_ROOT ?? "/tmp/antlr-cleanroom/antlr-ng-1f68422",
  };
  for (let index = 0; index < args.length; index += 1) {
    const argument = args[index];
    switch (argument) {
      case "--update":
        result.update = true;
        break;
      case "--check":
        result.update = false;
        break;
      case "--java-root":
        result.javaRoot = resolve(requiredValue(args, ++index, argument));
        break;
      case "--antlr-ng-root":
        result.antlrNgRoot = resolve(requiredValue(args, ++index, argument));
        break;
      default:
        throw new Error(`unknown argument: ${argument}`);
    }
  }
  if (result.update === null) {
    throw new Error(
      "usage: generate-phase-c-runtime-fixtures.mjs --check|--update",
    );
  }
  return result;
}

function requiredValue(args, index, option) {
  const value = args[index];
  if (value === undefined) {
    throw new Error(`${option} requires a value`);
  }
  return value;
}

function verifyCommit(root, expected, label) {
  const result = spawnSync("git", ["rev-parse", "HEAD"], {
    cwd: root,
    encoding: "utf8",
  });
  if (result.status !== 0 || result.stdout.trim() !== expected) {
    throw new Error(
      `${label} root must be at ${expected}; found ${result.stdout.trim() || result.stderr.trim()}`,
    );
  }
}

function gitText(root, object) {
  const result = spawnSync("git", ["show", object], {
    cwd: root,
    encoding: "utf8",
    maxBuffer: 32 * 1024 * 1024,
  });
  if (result.status !== 0) {
    throw new Error(`git show ${object} failed: ${result.stderr}`);
  }
  return result.stdout;
}

async function load(path) {
  return JSON.parse(await readFile(resolve(repositoryRoot, path), "utf8"));
}

async function expectFile(path, expected) {
  const actual = await readFile(path, "utf8");
  if (actual !== expected) {
    throw new Error(`${relative(repositoryRoot, path)} differs`);
  }
}

function digest(value) {
  return createHash("sha256").update(value).digest("hex");
}

function rustIdentifier(value) {
  return value.replaceAll("-", "_").replaceAll(/[^a-zA-Z0-9_]/gu, "_");
}

function rustString(value) {
  return JSON.stringify(value)
    .replaceAll("\u2028", "\\u{2028}")
    .replaceAll("\u2029", "\\u{2029}");
}

function renderRustSlice(values) {
  return `&[${values
    .map((value) =>
      typeof value === "string" ? rustString(value) : String(value),
    )
    .join(", ")}]`;
}

function parseNumberArray(value) {
  return value
    .slice(1, -1)
    .split(",")
    .map((part) => Number.parseInt(part.trim(), 10));
}

function escapeRegExp(value) {
  return value.replaceAll(/[.*+?^${}()|[\]\\]/gu, "\\$&");
}
