async function updateFixture(logicalId, fixture) {
  const directory = resolve(fixturesRoot, logicalId);
  await rm(directory, { recursive: true, force: true });
  await mkdir(directory, { recursive: true });
  for (const [path, contents] of fixture.files) {
    await mkdir(dirname(resolve(directory, path)), { recursive: true });
    await writeFile(resolve(directory, path), contents, "utf8");
  }
  const manifest = {
    ...fixture.manifest,
    files: Object.fromEntries(
      [...fixture.files.entries()]
        .sort(([left], [right]) => left.localeCompare(right))
        .map(([path, contents]) => [path, digest(contents)]),
    ),
  };
  await writeFile(
    resolve(directory, "fixture.json"),
    `${JSON.stringify(manifest, null, 2)}\n`,
    "utf8",
  );
}

async function checkFixture(logicalId, fixture) {
  const directory = resolve(fixturesRoot, logicalId);
  for (const [path, contents] of fixture.files) {
    await expectFile(resolve(directory, path), contents);
  }
  const actual = JSON.parse(
    await readFile(resolve(directory, "fixture.json"), "utf8"),
  );
  for (const key of [
    "schema_version",
    "roots",
    "logical_ids",
    "upstream_tests",
    "java_antlr_test",
    "antlr_ng_test",
    "expected",
    "phase_c_runtime_oracle",
  ]) {
    if (JSON.stringify(actual[key]) !== JSON.stringify(fixture.manifest[key])) {
      throw new Error(`${logicalId} fixture field ${key} differs`);
    }
  }
  for (const [path, expectedHash] of Object.entries(actual.files ?? {})) {
    const actualHash = digest(await readFile(resolve(directory, path)));
    if (actualHash !== expectedHash) {
      throw new Error(`${logicalId} fixture hash differs for ${path}`);
    }
  }
}

function renderCase(logicalId, definition) {
  const name = rustIdentifier(logicalId);
  const roots = renderRustSlice(definition.roots);
  switch (definition.kind) {
    case "lexer":
      return `lexer_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${renderLexerChecks(definition.checks)},\n);`;
    case "parser":
    case "grammar-parser":
      return `parser_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${definition.kind === "grammar-parser"},\n    ${renderParserChecks(definition.checks)},\n);`;
    case "parser-alt":
      return `parser_alt_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${renderAltChecks(definition.checks)},\n);`;
    case "prediction":
      return `prediction_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${renderPredictionChecks(definition.predictions)},\n    ${renderDfaChecks(definition.dfaChecks)},\n);`;
    case "left-recursion-alts":
      return `left_recursion_alts_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${rustString(definition.rule)},\n    ${renderRustSlice(definition.primary)},\n    ${renderRustSlice(definition.recursive)},\n);`;
    case "ambiguous":
      return `ambiguous_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${renderAmbiguousChecks(definition.checks)},\n);`;
    case "lexer-actions":
      return `lexer_action_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${rustString(definition.lexerName)},\n    ${rustString(definition.input)},\n    ${renderRustSlice(definition.expectedTokens)},\n    ${renderActionEvents(definition.actions)},\n    ${renderPredicateKind(definition.predicate)},\n    ${renderPredicateEvents(definition.predicates)},\n);`;
    case "parser-case-insensitive":
      return renderToolCase(
        name,
        logicalId,
        roots,
        `ToolCase::ParserRuntime { lexer: ${rustString(definition.lexerName)}, parser: ${rustString(definition.parserName)}, start_rule: ${rustString(definition.startRule)}, input: ${rustString(definition.input)} }`,
      );
    case "parser-start-without-eof":
      return renderToolCase(
        name,
        logicalId,
        roots,
        `ToolCase::ParserStartWithoutEof { lexer: ${rustString(definition.lexerName)}, parser: ${rustString(definition.parserName)}, start_rule: ${rustString(definition.startRule)}, input: ${rustString(definition.input)} }`,
      );
    case "parser-alternate-quotes":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::ParserAlternateQuotes",
      );
    case "parser-attribute-initialization":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::ParserAttributeInitialization",
      );
    case "parser-failed-predicate":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::ParserFailedPredicate",
      );
    case "parser-fail-option":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::ParserFailOption",
      );
    case "codegen-single-literal-accessor":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::CodegenLiteralAccessor { repeated: false }",
      );
    case "codegen-repeated-literal-accessor":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::CodegenLiteralAccessor { repeated: true }",
      );
    case "codegen-access-level":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::CodegenAccessLevel",
      );
    case "codegen-argument-declaration":
      return renderToolCase(
        name,
        logicalId,
        roots,
        "ToolCase::CodegenArgumentDeclaration",
      );
    default:
      throw new Error(`${logicalId} cannot render kind ${definition.kind}`);
  }
}

function renderToolCase(name, logicalId, roots, toolCase) {
  return `tool_case!(\n    ${name},\n    ${rustString(logicalId)},\n    ${roots},\n    ${toolCase},\n);`;
}

