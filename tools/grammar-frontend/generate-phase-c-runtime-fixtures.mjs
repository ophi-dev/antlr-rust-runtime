async function sourceMethod(testCase, root, commit, language) {
  const key = `${root}:${testCase.source.path}`;
  let source = sourceCache.get(key);
  if (source === undefined) {
    source = gitText(root, `${commit}:${testCase.source.path}`);
    sourceCache.set(key, source);
  }
  let methods = methodCache.get(key);
  if (methods === undefined) {
    methods =
      language === "java"
        ? extractJavaMethods(source)
        : extractTypeScriptMethods(source);
    methodCache.set(key, methods);
  }
  const method = methods.get(testCase.name);
  if (!method) {
    throw new Error(
      `${testCase.id} cannot locate ${testCase.source.path}#${testCase.name}`,
    );
  }
  return method;
}

function extractDefinition(logicalId, suite, method, language) {
  switch (suite) {
    case "TestATNLexerInterpreter":
      return extractLexerInterpreter(logicalId, method, language);
    case "TestParserInterpreter":
      return extractParserInterpreter(logicalId, method, language, false);
    case "TestGrammarParserInterpreter":
      return extractParserInterpreter(logicalId, method, language, true);
    case "TestATNInterpreter":
      return extractAtnInterpreter(logicalId, method, language);
    case "TestATNParserPrediction":
      return extractParserPrediction(logicalId, method, language);
    case "TestAmbigParseTrees":
      return extractAmbiguousTrees(logicalId, method, language);
    case "TestLexerActions":
      return extractLexerActions(logicalId, method, language);
    case "TestParserExec":
      return extractParserExec(logicalId, method, language);
    case "TestCodeGeneration":
      return extractCodeGeneration(logicalId, method, language);
    default:
      throw new Error(`${logicalId} has unsupported suite ${suite}`);
  }
}

function extractLexerInterpreter(logicalId, method, language) {
  const constructor = constructorArguments(method, "LexerGrammar", language)[0];
  const grammar = constructor
    ? evaluateString(
        constructor.arguments[0],
        method,
        constructor.offset,
        language,
      )
    : evaluateAssignedString("grammar", method, method.length, language);
  const checks = calls(method, "checkLexerMatches", language).map((call) => {
    const input = evaluateString(
      call.arguments[1],
      method,
      call.offset,
      language,
    );
    const expected = evaluateString(
      call.arguments[2],
      method,
      call.offset,
      language,
    )
      .split(", ")
      .filter((name) => name !== "EOF" && !lexerRuleSkips(grammar, name));
    expected.push("EOF");
    return { input, expected, errors: [] };
  });
  if (checks.length === 0) {
    const exec = calls(method, "execLexer", language)[0];
    if (!exec) {
      throw new Error(`${logicalId} has no lexer behavior invocation`);
    }
    const inputIndex = language === "java" ? 3 : 2;
    const input = evaluateString(
      exec.arguments[inputIndex],
      method,
      exec.offset,
      language,
    );
    const expectedError = firstExpectedError(method, language);
    checks.push({
      input,
      expected: null,
      errors: expectedError ? [expectedError.trimEnd()] : [],
    });
  }
  return {
    kind: "lexer",
    roots: ["L.g4"],
    files: new Map([["L.g4", grammar]]),
    checks,
  };
}

function extractLexerActions(logicalId, method, language) {
  const exec = calls(method, "execLexer", language)[0];
  if (!exec) {
    throw new Error(`${logicalId} has no execLexer invocation`);
  }
  const grammar = evaluateAssignedString(
    "grammar",
    method,
    exec.offset,
    language,
  );
  const root = evaluateString(exec.arguments[0], method, exec.offset, language);
  const lexerName = evaluateString(
    exec.arguments[2],
    method,
    exec.offset,
    language,
  );
  const input = evaluateString(
    exec.arguments[3],
    method,
    exec.offset,
    language,
  );
  const expected = evaluateAssignedString(
    "expecting",
    method,
    method.length,
    language,
  );
  const expectedTokens = expected
    .split("\n")
    .filter((line) => line.startsWith("[@"));
  if (expectedTokens.length === 0) {
    throw new Error(`${logicalId} has no expected token stream`);
  }
  return {
    kind: "lexer-actions",
    roots: [root],
    files: new Map([[root, grammar]]),
    lexerName,
    input,
    expectedTokens,
    ...lexerActionOracle(logicalId),
  };
}

